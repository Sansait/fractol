#include "fractol.h"

void	draw(t_data *d)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (y < HEIGHT)
	{
		while (x < WIDTH)
		{
			mandelbrot(x, y, d);
			x++;
		}
		x = 0;
		y++;
	}
}
